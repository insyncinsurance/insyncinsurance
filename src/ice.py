def gt(input, comparative):
    if input > comparative:
        return True
    else:
        return False

def ge(input, comparative):
    if input >= comparative:
        return True
    else:
        return False

def lt(input, comparative):
    if input < comparative:
        return True
    else:
        return False

def le(input, comparative):
    if input <= comparative:
        return True
    else:
        return False

